package com.laguna.sergio.ecolife;

public class DataAdapterTalo {

    String NroTalo;
    String FechaTalo;
    String Estado;
    String NroVentas;
    String NroVentasCompletadas;



    public String getNroTalo() {

        return NroTalo;
    }

    public void setNroTalo(String NroTalo) {

        this.NroTalo = NroTalo;
    }

    public String getFechaTalo() {

        return FechaTalo;
    }

    public void setFechaTalo(String FechaTalo) {

        this.FechaTalo = FechaTalo;
    }
    public String getEstado(){
        return Estado;
    }
    public void setEstado(String estado){
        this.Estado=estado;
    }
    public String getNroVentas() {
        return NroVentas;
    }

    public void setNroVentas(String nroVentas) {
        NroVentas = nroVentas;
    }
    public String getNroVentasCompletadas() {
        return NroVentasCompletadas;
    }

    public void setNroVentasCompletadas(String nroVentasCompletadas) {
        NroVentasCompletadas = nroVentasCompletadas;
    }




}
